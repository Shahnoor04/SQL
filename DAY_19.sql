/* DAY-19: For each service, rank the weeks by patient satisfaction score (highest first). 
Show service, week, patient_satisfaction, patients_admitted, and the rank. Include only the top 3 weeks per service. */

SELECT * FROM(
SELECT service, week, patient_satisfaction, patients_admitted, 
RANK() OVER(PARTITION BY service ORDER BY patient_satisfaction DESC) AS rnk
FROM services_weekly
) AS ranked
 WHERE rnk <=3;

-- Other Practice Queries:
-- 1. Rank patients by satisfaction score within each service.

SELECT patient_id, name AS patient_name, age, service, satisfaction,
DENSE_RANK()OVER (PARTITION BY service ORDER BY satisfaction DESC) AS d_rank
FROM patients;

-- 2. Assign row numbers to staff ordered by their name.

SELECT staff_id, staff_name,
ROW_NUMBER() OVER(ORDER BY staff_name ASC) AS row_num
FROM staff;

-- 3. Rank services by total patients admitted.

SELECT service, SUM(patients_admitted) AS total_patients_admitted,
RANK() OVER(ORDER BY SUM(patients_admitted) DESC) AS rnk
FROM services_weekly
GROUP BY service;
